<?php

# Підключаємо клас DeviceDetector для визначення типу пристрою.
use Nucleus\Helpers\DeviceDetector;

# Перевіряємо, чи є пристрій мобільним.
if (DeviceDetector::isMobile()) {
    # Якщо пристрій мобільний, виводимо відповідне повідомлення.
    echo "Це мобільний пристрій.";
} else {
    # Якщо пристрій не мобільний (десктоп або інший), виводимо інше повідомлення.
    echo "Це десктопний пристрій.";
}
